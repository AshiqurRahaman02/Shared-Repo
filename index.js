// index.js

// Function to multiply a number by 5
function multiplyByFive(num) {
    return num * 5;
}

// Function to multiply a number by 9
function multiplyByNine(num) {
    return num * 9;
}

function divideByFive(num) {
    return num / 5;
}

function divideByNine(num) {
    return num / 9;
}

module.exports = { multiplyByFive, multiplyByNine, divideByFive, divideByNine };
